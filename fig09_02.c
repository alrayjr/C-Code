#include <stdio.h>
int main(void)
{
    printf("%d\n", 455);
    printf("%i\n", 455);
    printf("%d\n", +455);
    printf("%+d\n", +455);
    printf("%d\n\n", -455);
    //comment
    printf("%hd\n", 32000);
    printf("%hd\n", 32770);
    printf("%ld\n", 200000000L);
    printf("%d\n",  2000000000L);
    printf("%d\n",  20000000044);
    printf("%o\n",  455);
    printf("%u\n",  455);
    printf("%u\n", -455);
    printf("%x\n",  455);
    printf("%X\n",  455);
    printf("%s", "C Programming class");
    printf("C Programming class");
}
